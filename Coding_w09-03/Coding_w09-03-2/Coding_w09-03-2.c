//ใช้ while + continue
#include <stdio.h>              //เรียกฟังก์ชัน library มาตรฐาน

int main(){                     //ฟังก์ชันหลัก
    int i = 0;                  //กำหนดตัวแปร i เท่ากับ 0
 
    while (i < 10){             //วนลูป 0 ถึง 9
          i++;                  // เพื่อค่า i ให้ i = i + 1
        if(i == 6){             //i เท่ากับ 6 หรือไม่
            continue;           //ถ้าใช่ ข้ามไปรอบถัดไป (ไม่ทำprinf)
        }
        printf("%d ", i);       //แสดงค่า i (ไม่แสดงค่า 6)
    }
    return 0;                   //จบโปรแกรม
}

// === อธิบายการทำงานของโปรแกรม ===
// 1. กำหนดตัวแปร i
// 2. วนลูป 0 ถึง 9
// 3. ให้ i = i + 1 
// 4. ตรวจสอบว่า i เท่ากับ 6 หรือไม่
// 5. ถ้าใช่ ข้ามไปรอบถัดไป (ไม่ทำprinf)
// 6. แสดงค่า i (ไม่แสดงค่า 6)
// 7. จบโปรแกรม


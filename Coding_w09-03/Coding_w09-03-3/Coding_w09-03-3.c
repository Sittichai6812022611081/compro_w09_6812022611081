//do-while + continue
#include <stdio.h>              //เรียกฟังก์ชัน library มาตรฐาน

int main(){                     //ฟังก์ชันหลัก
    int i = 0;                  //กำหนดตัวแปร i เท่ากับ 0

    do {                        //เริ่มลูป do-while
        i++;                    //เพิ่มค่า i ให้ i = i + 1
        if(i == 6){             //ตรวจสอบว่า i เท่ากับ 6 หรือไม่
            continue;           //ถ้าใช่ ข้ามไปรอบถัดไปหรือข้ามไปตรวจสอบเงือนไข while (ไม่ทำprintf)
        }
        printf("%d ", i);       //แสดงค่า i (ไม่แสดงค่า 6)
    } while (i < 10);           //วนลูป 0 ถึง 9
    
    return 0;                   //จบโปรแกรม
}

// === อธิบายการทำงานของโปรแกรม ===                                                // === สรุปความแตกต่าง ===
// 1. กำหนดตัวแปร i                                                               // 1. FOR LOOP:
// 2. เริ่มลูป do-while ให้ i = i + 1                                                 //    - กะทัดรัด ใช้บรรทัดเดียว
// 3. ตรวจสอบว่า i เท่ากับ 6 หรือไม่                                                   //    - เหมาะกับการวนลูปที่รู้จำนวนรอบแน่นอน
// 4. ถ้าใช่ ข้ามไปรอบถัดไปหรือข้ามไปตรวจสอบเงือนไข while (ไม่ทำprintf)                  //    - ตัวแปร i ถูกควบคุมในหัว for
// 5. แสดงค่า i (ไม่แสดงค่า 6)
// 6. วนลูป 0 ถึง 9                                                                // 2. WHILE LOOP:
// 7. จบโปรแกรม                                                                   //    - ตรวจสอบเงื่อนไขก่อนเข้าลูป
                                                                                  //    - ถ้าเงื่อนไขเป็นเท็จตั้งแต่แรก จะไม่เข้าลูปเลย
                                                                                  //    - ยืดหยุ่นในการใช้งาน

                                                                                  // 3. DO-WHILE LOOP:
                                                                                  //    - ทำก่อน แล้วค่อยตรวจสอบเงื่อนไข
                                                                                  //    - รันอย่างน้อย 1 รอบเสมอ
                                                                                  //    - เหมาะกับงานที่ต้องทำอย่างน้อย 1 ครั้ง

                                                                                  // === ผลลัพธ์เหมือนกัน ===
                                                                                  // ทั้ง 3 โปรแกรมจะแสดงผล: 1 2 3 4 5 7 8 9 10
                                                                                  // (ข้าม 6 เพราะ continue)

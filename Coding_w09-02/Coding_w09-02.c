#include <stdio.h>                                      //เรียกฟังก์ชัน library มาตรฐาน

int main(){                                             //ฟังก์ชันหลัก
    int sum, p, x, y;                                   //กำหนดตัวแปร sum, p, x, y
    sum = 0;                                            //ให้ sum เท่ากับ 0
    for (x = 1, y = 1; x * y <= 15; x++, y += 2 )       //ให้ x เท่ากับ 1, y เท่ากับ 1, x คูณ y น้อยกว่าหรือเท่ากับ 15, x บวก 1 และ  y บวก 2  หลังจากจบloop body ในแต่ละรอบ
    {
        p = x * y;                                       // p เท่ากับ x คูณ y
        sum = sum + p;                                   //sum เท่ากับ sum บวก p
        printf("%d * %d = %d\n", x, y, p);               //แสดงค่าที่ได้ของ x คูณ ค่าที่ได้ของ y เท่ากับ p
    }
    printf("summation of x * y = %d\n", sum);            //แสดงข้อความ summation of x * y = ค่าที่ได้ของ sum
    return 0;                                            //จบโปรแกรม
}

// === การทำงานของโปรแกรม ===
// 1. เริ่มต้น: x=1, y=1, sum=0
// 2. เข้าวนลูปตราบใด x*y <= 15
// 3. ในแต่ละรอบ:
//    - คำนวณ p = x * y
//    - เพิ่ม sum = sum + p
//    - แสดงผล x * y = p
//    - เพิ่ม x ทีละ 1, เพิ่ม y ทีละ 2 
//    - วนลูปจน ไม่เข้าเงื่อนใข x * y <= 15
//    - จากนั้น แสดงข้อความ summation of x * y = ค่าที่ได้ของ sum (sum = ผลรวมทั้งหมดตั้งแต่รอบแรกจนถึงรอบสุดท้าย)

// === การทำงานแต่ละรอบ ===
// รอบที่ 1: x=1, y=1 → 1*1=1 ≤ 15  ture คือ  p=1, sum=1, แสดง "1 * 1 = 1"
// รอบที่ 2: x=2, y=3 → 2*3=6 ≤ 15 ture คือ p=6, sum=7, แสดง "2 * 3 = 6"  
// รอบที่ 3: x=3, y=5 → 3*5=15 ≤ 15 ture คือ p=15, sum=22, แสดง "3 * 5 = 15"
// รอบที่ 4: x=4, y=7 → 4*7=28 > 15 false คือ ออกจากลูป

// === ผลลัพธ์ที่แสดง ===
// 1 * 1 = 1
// 2 * 3 = 6
// 3 * 5 = 15
// summation of x * y = 22

// === สรุปและอธิบายผลการทดลอง ===
// 1. โปรแกรมใช้ for loop
// 2. ตัวแปร x เพิ่มทีละ 1, ตัวแปร y เพิ่มทีละ 2 แต่รอบแรกจะไม่เพิ่ม
// 3. เงื่อนไขหยุดคือเมื่อ x*y > 15
// 4. มีการสะสมผลรวมของ x*y ทุกรอบ
// 5. แสดงผลทั้งการคำนวณแต่ละรอบและผลรวมสุดท้าย

